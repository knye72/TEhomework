using System;

namespace LoopsArraysTutorial
{
    class Program
    {
        static void Main(string[] args)
        {
            // write your code here
            int i;
            for (i = 0; i <= 5; i++)
            {
                Console.WriteLine(i);
            }
            for (i = 10; i >= 0; i--)
            {
                Console.WriteLine(i);
            }

        }
    }
}
